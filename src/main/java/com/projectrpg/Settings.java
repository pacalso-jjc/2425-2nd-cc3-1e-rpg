public class Setting {

    private int textDelay;

    // Constructor
    public Setting(int textDelay) {
        this.textDelay = textDelay;
    }

    // Getter for textDelay
    public int getTextDelay() {
        return textDelay;
    }

    // Setter for textDelay
    public void setTextDelay(int textDelay) {
        this.textDelay = textDelay;
    }
}